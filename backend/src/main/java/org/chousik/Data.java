package org.chousik.chousik;

public class Data {
    private int x;
    private double y;
    private double r;

    public Data(int x, double y, double r){
        this.x = x;
        this.y = y;
        this.r = r;
    }

    public int getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getR() {
        return r;
    }
}
